import type { Locale } from "@/i18n/config";
import type { AuthorSlug } from "@/data/authors";

type L = Record<Locale, string>;

/** Розділ статті (h2 + абзаци + опційна таблиця + контекстні лінки). id — якір для змісту/сітлінків. */
export type BlogSection = {
  /** Якір: /^[a-z0-9-]+$/, унікальний у межах статті. */
  id: string;
  h: L;
  p: Record<Locale, string[]>;
  table?: { columns: L[]; rows: L[][] };
  /** Контекстні лінки на каталог у межах розділу (перелінковка). БЕЗ цін/кнопок. */
  links?: BlogLink[];
};

/** Часті запитання → FAQPage schema + видимий блок. */
export type BlogFaq = { q: L; a: L };

/** «Думка магазину» — врізка від першої особи автора. Тільки особистий досвід. */
export type BlogOpinion = { body: L };

/** Контекстний лінк на товар/категорію — БЕЗ цін і кнопок (щоб не канібалізувати каталог). */
export type BlogLink = { label: L; href: string };

export type BlogPost = {
  /** URL-сегмент: /{locale}/blog/<slug>. Регулярка: /^[a-z0-9-]{1,80}$/. */
  slug: string;
  /** H1 — питальний/інфо-формат (правило формату SEO-PLAN). */
  h1: L;
  /** <title> повний, з брендом. */
  metaTitle: L;
  metaDescription: L;
  /** Короткий опис для картки в списку + fallback og:description. */
  excerpt: L;
  /** ISO-дата (YYYY-MM-DD). Ставиться вручну, не new Date(). */
  datePublished: string;
  dateModified: string;
  author: AuthorSlug;
  /** Cloudinary publicId обкладинки (1200×630 для og). undefined — рендер без фото. */
  heroPublicId?: string;
  /** Фонове зображення для фірмової обкладинки (public-шлях). Поверх — лого й заголовок кодом. */
  coverSrc?: string;
  heroAlt: L;
  heroCaption?: L;
  /** Вступні абзаци (над змістом). */
  intro: Record<Locale, string[]>;
  sections: BlogSection[];
  opinion?: BlogOpinion;
  faq?: BlogFaq[];
  /** Посилання на офіційне першоджерело (напр. правила ITTF). Рендериться як зовнішній лінк. */
  officialSource?: { label: L; url: string };
  /** Контекстні лінки на товар (перелінковка → каталог). */
  relatedProducts?: BlogLink[];
  /** Слаги інших статей для блоку «Читайте також» (стаття → стаття). */
  relatedPosts?: string[];
  /** Інтерактивний блок для залучення. playStyle — квіз стилю; racketPicker — підбір ракетки за рівнем. */
  interactive?: "playStyle" | "racketPicker";
  /** true → не рендериться на проді й не потрапляє в sitemap/список. */
  draft?: boolean;
};

